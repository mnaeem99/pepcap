import { Component, OnInit, Inject } from '@angular/core';
import { ActivatedRoute,Router} from "@angular/router";
import { FormArray, FormBuilder, FormControl, Validators } from '@angular/forms';
import { MatDialogRef, MatDialog, MAT_DIALOG_DATA } from '@angular/material/dialog';

import { SuppliersService } from '../suppliers.service';
 import { ISuppliers
 } from '../isuppliers';
import { BaseNewComponent, FieldType, PickerDialogService } from 'src/app/common/shared';
import { ErrorService } from 'src/app/core/services/error.service';


@Component({
  selector: 'app-suppliers-new',
  templateUrl: './suppliers-new.component.html',
  styleUrls: ['./suppliers-new.component.scss']
})
export class SuppliersNewComponent extends BaseNewComponent<ISuppliers> implements OnInit {
  
    title:string = "New Suppliers";
	constructor(
		public formBuilder: FormBuilder,
		public router: Router,
		public route: ActivatedRoute,
		public dialog: MatDialog,
		public dialogRef: MatDialogRef<SuppliersNewComponent>,
		@Inject(MAT_DIALOG_DATA) public data: any,
		public pickerDialogService: PickerDialogService,
		public suppliersService: SuppliersService,
		public errorService: ErrorService,
	) {
		super(formBuilder, router, route, dialog, dialogRef, data, pickerDialogService, suppliersService, errorService);
	}
 
		ngOnInit() {
		this.entityName = 'Suppliers';
		this.setAssociations();
		super.ngOnInit();
    	this.setForm();
		this.checkPassedData();
        }


	setForm(){
 		this.itemForm = this.formBuilder.group({
      contactInfo: [''],
      createdAt: [''],
	  createdAtTime: ['12:00 AM'],
      name: ['', Validators.required],
    });
    
    this.fields = [
              {
      		  name: 'name',
      		  label: 'name',
      		  isRequired: true,
      		  isAutoGenerated: false,
      	      type: FieldType.String,
      	    },
              {
      		  name: 'createdAt',
      		  label: 'created At',
      		  isRequired: false,
      		  isAutoGenerated: false,
      		  type: FieldType.Date,
      		},
      		{
      		  name: 'createdAtTime',
      		  label: 'created At Time',
      		  isRequired: false,
      		  isAutoGenerated: false,
      		  type: FieldType.Time
      	    },
              {
      		  name: 'contactInfo',
      		  label: 'contact Info',
      		  isRequired: false,
      		  isAutoGenerated: false,
      	      type: FieldType.String,
      	    },
		];
	}
	 
	setAssociations(){
  	
		this.associations = [
		];
		this.parentAssociations = this.associations.filter(association => {
			return (!association.isParent);
		});

	}
	
	onSubmit() {
		let suppliers = this.itemForm.getRawValue();


	    suppliers.createdAt = this.dataService.combineDateAndTime(suppliers.createdAt, suppliers.createdAtTime);
	    delete suppliers.createdAtTime;

        super.onSubmit(suppliers);

	}
    
}
